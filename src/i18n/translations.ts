export type Language = 'en' | 'de';

export function getTranslation(lang: Language, key: string): string {
  const keys = key.split('.');
  let value: any = translations[lang];

  for (const k of keys) {
    value = value[k];
    if (value === undefined) {
      console.warn(`Translation key not found: ${key} for language: ${lang}`);
      return key;
    }
  }

  return value;
}
